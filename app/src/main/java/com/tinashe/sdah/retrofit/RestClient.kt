package com.tinashe.sdah.retrofit

import com.tinashe.sdah.BuildConfig
import io.reactivex.schedulers.Schedulers
import okhttp3.OkHttpClient
import okhttp3.logging.HttpLoggingInterceptor
import retrofit2.Retrofit
import retrofit2.adapter.rxjava2.RxJava2CallAdapterFactory
import retrofit2.converter.gson.GsonConverterFactory
import java.util.concurrent.TimeUnit

/**
 * Created by tinashe on 2017/11/12.
 */

object RestClient {

    private val TIMEOUT_SECONDS = 60

    private val client: OkHttpClient
        get() {
            val clientBuilder = OkHttpClient.Builder()
            clientBuilder.readTimeout(TIMEOUT_SECONDS.toLong(), TimeUnit.SECONDS)
            clientBuilder.connectTimeout(TIMEOUT_SECONDS.toLong(), TimeUnit.SECONDS)

            if (BuildConfig.DEBUG) {
                val interceptor = HttpLoggingInterceptor()
                interceptor.level = HttpLoggingInterceptor.Level.BODY
                clientBuilder.addInterceptor(interceptor)
            }

            return clientBuilder.build()
        }

    private fun dynamicRetrofit(baseUrl: String): Retrofit {
        return Retrofit.Builder()
                .baseUrl(baseUrl)
                .addConverterFactory(GsonConverterFactory.create())
                .addCallAdapterFactory(RxJava2CallAdapterFactory.createWithScheduler(Schedulers.io()))
                .client(client)
                .build()
    }

    fun <T> createService(service: Class<T>, baseUrl: String): T {
        return dynamicRetrofit(baseUrl).create(service)
    }
}
